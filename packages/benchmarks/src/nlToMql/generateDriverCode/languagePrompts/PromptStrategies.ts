/**
   - `chainOfThought`: Use chain of thought reasoning to generate the query.
   - `default`: Prompt-engineering optimized prompt.
   - `lazy`: Minimal viable prompt. Useful for baselining.
 */
export type SystemPromptStrategy = "chainOfThought" | "default" | "lazy";

/**
  - `annotated`: Use annotated schema. Generated by human/LLM.
  - `interpreted`: Use interpreted schema. Interpreted by code from database, no 'intelligence' involved.
  - `none`: Do not include schema.
 */
export type SchemaStrategy = "annotated" | "interpreted" | "none";
